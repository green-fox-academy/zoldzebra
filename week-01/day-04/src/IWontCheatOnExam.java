// Create a program that writes this line 100 times:
// "I won't cheat on the exam!"

public class IWontCheatOnExam {

    public static void main(String[] args) {
        int a = 1;
        while (a < 101) {
            System.out.println(a + ". I won't cheat on the exam!");
            a++;
        }

    }
}
