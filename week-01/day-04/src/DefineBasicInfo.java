public class DefineBasicInfo {

    public static void main(String[] args) {
        // Define several things as a variable then print their values
        // Your name as a string
        // Your age as an integer
        // Your height in meters as a double
        // Whether you are married or not as a boolean

        String myName = "Daniel";
        int myAge = 37;
        double myHeight = 1.85;
        boolean maritalStatus = false;

        System.out.println("His name is " + myName + ".");
        System.out.println(myName + "'s age is: " + myAge + " years.");
        System.out.println("His height is " + myHeight + " m.");
        System.out.println("If he would say he's married that would be completely " + maritalStatus + ".");
    }

}
